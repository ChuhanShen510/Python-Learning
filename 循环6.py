import numpy
r = input('请输入今年的利润：')
r = int(r)

if r <= 100000:
    print('应该发放的奖金总数：', r*0.1)
elif 100000 < r <= 200000: 
    print('应该发放的奖金总数：', 0.075*(r-100000)+100000*0.1)
elif 200000 < r <= 400000:
    print('应该发放的奖金总数：', 0.05*(r-200000)+100000*0.075+100000*0.1)
elif 400000 < r <= 600000:
    print('应该发放的奖金总数：', 0.03*(r-400000)+200000*0.05+100000*0.075+100000*0.1)
elif 600000 < r <= 1000000:
    print('应该发放的奖金总数：', 0.015*(r-600000)+0.03*200000+200000*0.05+100000*0.075+100000*0.1)
elif 1000000 < r:
    print('应该发放的奖金总数：', 0.01*(r-1000000)+0.015*400000+0.03*200000+200000*0.05+100000*0.075+100000*0.1)
